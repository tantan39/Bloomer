#import "API_CaptionEdit.h"

@implementation API_CaptionEdit

- (instancetype)initWithParams:(caption_post *)params{
    self = [super initWith:[APIDefine post_captionLink] Params:[params encodeToJSON]];
    return self;
}

- (BaseJSON *)handleJSON:(NSDictionary *)json{
    if (json) {
        return  (BaseJSON *)[[out_name_update alloc] initWithJSON:json];
    }
    return (BaseJSON *) [[out_name_update alloc] init];
}

@end
